def _slugify(name: str) -> str:
    """Convert a scenario name to a safe directory-name slug."""
    slug = re.sub(r"[^\w\s-]", "", name.lower())
    slug = re.sub(r"[\s_]+", "_", slug).strip("_-")
    return slug[:40]  # cap length


def _build_docker_compose(topology: NetworkTopology) -> str:
    """Generate a docker-compose.yml for runtime deployment of all boxes."""
    lines = [
        "# Auto-generated by Game of Everything",
        f"# Scenario: {topology.scenario_name}",
        "",
        "networks:",
        "  goe_net:",
        "    driver: bridge",
        "",
        "services:",
    ]

    ordered = topological_order(topology)
    for box in ordered:
        lines.append(f"  {box.box_id}:")
        lines.append(f"    image: {box.os}")
        lines.append(f"    hostname: {box.hostname}")
        lines.append("    networks:")
        lines.append("      - goe_net")

        # Expose declared services as host-mapped ports (sequential offset to avoid collisions)
        if box.services:
            idx = ordered.index(box)
            lines.append("    ports:")
            for svc in box.services:
                # e.g. "http:80" or "ssh:22"
                parts = svc.split(":")
                if len(parts) == 2:
                    try:
                        container_port = int(parts[1])
                        host_port = container_port + idx * 1000
                        lines.append(f'      - "{host_port}:{container_port}"')
                    except ValueError:
                        pass

        lines.append(f"    volumes:")
        lines.append(f"      - ./{box.box_id}_deploy.sh:/opt/deploy.sh:ro")
        lines.append(
            "    entrypoint: "
            '["bash", "-c", "chmod +x /opt/deploy.sh && /opt/deploy.sh && exec sleep infinity"]'
        )
        lines.append("")

    return "\n".join(lines)


def _build_playbook(topology: NetworkTopology, deploy_scripts: Dict[str, str]) -> dict:
    """Build the machine-readable attack chain playbook."""
    attack_chain = []

    # Step 0: entry-point exploitation (one step per entry box)
    for ep in topology.entry_point:
        entry_box = next(b for b in topology.boxes if b.box_id == ep)
        attack_chain.append({
            "step": 0,
            "box": ep,
            "hostname": entry_box.hostname,
            "action": f"Exploit entry-point box: {entry_box.role}",
            "tool": "See per-box deploy script",
        })

    # Steps 1..N: pivot steps derived from topology
    for i, pivot in enumerate(topology.pivots):
        secret = next(
            (s for s in topology.shared_secrets if s.key == pivot.secret_ref), None
        )
        target_box = next(
            (b for b in topology.boxes if b.box_id == pivot.to_box), None
        )
        step: dict = {
            "step": i + 1,
            "box": pivot.to_box,
            "hostname": target_box.hostname if target_box else pivot.to_box,
            "action": pivot.description,
            "pivot_method": pivot.method,
        }
        if secret:
            step["artifact"] = f"{secret.target_user}:{secret.value}" if secret.access_method != "ssh_key" else "SSH private key"
            step["access_method"] = secret.access_method
        attack_chain.append(step)

    return {
        "scenario_name": topology.scenario_name,
        "entry_point": topology.entry_point,
        "attack_narrative": topology.attack_narrative,
        "attack_chain": attack_chain,
        "shared_secrets": [
            {
                "key": s.key,
                "value": s.value,
                "user": s.target_user,
                "description": s.description,
                "access_method": s.access_method,
                "source_box": s.source_box,
                "target_box": s.target_box,
            }
            for s in topology.shared_secrets
        ],
        "chain_probes": [
            {
                "step": cp.step,
                "from_container": cp.from_container,
                "target_hostname": cp.target_hostname,
                "command": cp.command,
                "success_pattern": cp.success_pattern,
            }
            for cp in topology.chain_probes
        ],
    }


def _build_readme(topology: NetworkTopology, deploy_scripts: Dict[str, str], credential_warnings: List[str] = []) -> str:
    """Generate a human-readable README for the scenario."""
    lines = [
        f"# {topology.scenario_name}",
        "",
        "## Scenario Overview",
        "",
        topology.narrative,
        "",
        "## Attack Path",
        "",
        topology.attack_narrative,
        "",
        "## Network Topology",
        "",
        f"**Entry Point{'s' if len(topology.entry_point) > 1 else ''}**: {', '.join(f'`{ep}`' for ep in topology.entry_point)}",
        "",
        "| Box ID | Hostname | Role |",
        "|--------|----------|------|",
    ]
    for box in topological_order(topology):
        entry_tag = " *(entry)*" if box.box_id in topology.entry_point else ""
        lines.append(f"| `{box.box_id}` | `{box.hostname}` | {box.role}{entry_tag} |")

    if topology.pivots:
        lines += [
            "",
            "## Pivot Chain",
            "",
        ]
        for p in topology.pivots:
            lines.append(f"- **{p.from_box}** → **{p.to_box}** `[{p.method}]`: {p.description}")

    if topology.shared_secrets:
        lines += [
            "",
            "## Shared Secrets",
            "",
            "| Key | User | Value | Access Method | Source Box → Target Box |",
            "|-----|------|-------|---------------|------------------------|",
        ]
        for s in topology.shared_secrets:
            lines.append(
                f"| `{s.key}` | `{s.target_user}` | `{s.value}` "
                f"| {s.access_method} | {s.source_box} → {s.target_box} |"
            )

    lines += [
        "",
        "## Quick Start",
        "",
        "```bash",
        "docker-compose up -d",
        "```",
        "",
        "Startup takes 2–3 minutes per box (packages install at container start).",
        "",
        "## Per-Box Scripts",
        "",
    ]
    for box in topological_order(topology):
        script_size = len(deploy_scripts.get(box.box_id, ""))
        lines.append(f"- `{box.box_id}_deploy.sh` — {box.role} ({script_size} chars)")

    if topology.chain_probes:
        lines += [
            "",
            "## Chain Test Probes",
            "",
            "Run these from the attacker container to validate the attack chain:",
            "",
        ]
        for cp in topology.chain_probes:
            lines.append(f"**Step {cp.step}** (target: `{cp.target_hostname}`):")
            lines.append(f"```bash")
            lines.append(cp.command)
            lines.append("```")
            lines.append("")

    if topology.explicit_decisions:
        lines += [
            "## Synthesis Decisions",
            "",
            "The following were decided by the synthesis agent, not specified in the prompt:",
            "",
        ]
        for dec in topology.explicit_decisions:
            lines.append(f"- {dec}")

    if credential_warnings:
        lines += [
            "",
            "## \u26a0\ufe0f Credential Warnings",
            "",
            "The following shared secrets could not be verified in their deploy scripts:",
            "",
        ]
        for w in credential_warnings:
            lines.append(f"- {w}")

    return "\n".join(lines)



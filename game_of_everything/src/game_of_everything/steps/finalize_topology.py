"""Step: Finalize multi-box topology output package.

Single-box: no-op — output was already written by run_finalize_script.

Multi-box: writes a self-contained output directory containing:
  - {box_id}_deploy.sh     per-box deployment scripts
  - docker-compose.yml     orchestration (runtime deployment, not build-time)
  - playbook.json          machine-readable attack chain
  - README.md              human-readable guide
"""

import json
import re
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional, TYPE_CHECKING

import rich

from game_of_everything.models import NetworkTopology
from game_of_everything.state import GoEState
from game_of_everything.topology_utils import topological_order

if TYPE_CHECKING:
    from game_of_everything.ui import GoEConsole

# Project root: steps/ → game_of_everything/ → src/ → game_of_everything/ → (project root)
_PROJECT_ROOT = Path(__file__).parent.parent.parent.parent


